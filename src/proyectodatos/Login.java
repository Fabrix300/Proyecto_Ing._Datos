    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String [] usuarios = {"JGONZALES", "AMONTOYA"};
        String [] claves = {"clave1","clave2"};
        Integer [] codigos = {4001, 4002};
        int index = 0;
        String user = txtUser.getText();
        char[] pass = txtPassword.getPassword();
        String password = "";
        for (int i = 0; i < pass.length; i++) {
            password = password + pass[i];
        }
        boolean login = false;
        for(int i=0; i < usuarios.length; i++){
            if (usuarios[i].equals(user) && claves[i].equals(password)){
                login = true;
                index = i;
            }
        }
        if (login){
            Interfaz interfaz = new Interfaz(txtUser.getText(), codigos[index]);
            interfaz.setVisible(true);
            this.dispose();
        } else{
            JOptionPane.showMessageDialog(null, "La Clave/Usuario son incorrectos");
            txtPassword.setText("");
            txtUser.setText("");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

